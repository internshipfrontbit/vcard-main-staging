        function calculateEarnings() {
            let years = document.getElementById('years').value;
            let commissionPercent = document.getElementById('commission').value / 10;
            let totalCells = document.querySelectorAll('.total');
            let qtyInputs = document.querySelectorAll('.qty');
            let baseCommissions = [1500, 36.5, 600];
            let grandTotal = 0;
            
            totalCells.forEach((cell, index) => {
                let qty = qtyInputs[index].value;
                let total = baseCommissions[index] * qty * years * commissionPercent;
                cell.textContent = `₹${total.toFixed(2)}`;
                grandTotal += total;
            });
            
